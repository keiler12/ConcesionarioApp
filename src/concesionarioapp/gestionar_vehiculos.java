    private void cargarCatalogo() {
    jPanel2.removeAll();

    java.io.File archivo = new java.io.File("baseDeDatos/coches.csv");
    if (!archivo.exists()) {
        javax.swing.JOptionPane.showMessageDialog(this, "No hay veh칤culos guardados todav칤a.");
        return;
    }

    try (var br = new java.io.BufferedReader(new java.io.FileReader(archivo))) {
        String linea;
        int y = 10;

        // Saltar encabezado si lo hay
        br.readLine();

        while ((linea = br.readLine()) != null) {
            String[] datos = linea.split(",");

            if (datos.length == 5) {
                javax.swing.JPanel item = new javax.swing.JPanel();
                item.setLayout(new javax.swing.BoxLayout(item, javax.swing.BoxLayout.Y_AXIS));
                item.setBorder(javax.swing.BorderFactory.createTitledBorder(datos[0] + " - " + datos[1]));

                item.add(new javax.swing.JLabel("A침o: " + datos[2]));
                item.add(new javax.swing.JLabel("Precio: " + datos[3]));
                item.add(new javax.swing.JLabel("Descripci칩n: " + datos[4]));

                item.setBounds(10, y, 400, 90);
                y += 100;
                jPanel2.add(item);
            }
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar el cat치logo: " + e.getMessage());
    }

    jPanel2.revalidate();
    jPanel2.repaint();
}

    
    
    
    
    private void btnguardarvehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarvehiculoActionPerformed
        // TODO add your handling code here:
String marca = txtmarca.getText().trim();
    String modelo = txtmodelo.getText().trim();
    String a침o = txta침o.getText().trim();
    String precio = txtprecio.getText().trim();
    String kilometraje = txtKilometraje.getText().trim();
    String descripcion = txtdescripcion.getText().trim();
    String placa = txtPlaca.getText().trim();

    // Estos vienen del propietario
    String nombreProp = txtNombreProp.getText().trim();
    String cedulaProp = txtCedulaProp.getText().trim();
    String telefonoProp = txtTelefonoProp.getText().trim();
    String direccionProp = txtDireccionProp.getText().trim();
    String emailProp = txtEmailProp.getText().trim();

    // Ruta de imagen (guardada por tu bot칩n aparte)
    if (rutaImagenSeleccionada == null || rutaImagenSeleccionada.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor selecciona una imagen del veh칤culo.");
        return;
    }

    // Validar campos vac칤os
    if (marca.isEmpty() || modelo.isEmpty() || a침o.isEmpty() || precio.isEmpty() || descripcion.isEmpty()
            || placa.isEmpty() || nombreProp.isEmpty() || cedulaProp.isEmpty() || telefonoProp.isEmpty()
            || direccionProp.isEmpty() || emailProp.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor completa todos los campos antes de guardar.");
        return;
    }

    try {
        File archivo = new File("baseDeDatos/coches.csv");
        boolean nuevo = !archivo.exists();

        FileWriter fw = new FileWriter(archivo, true);
        BufferedWriter bw = new BufferedWriter(fw);

        if (nuevo) {
            bw.write("marca,modelo,a침o,precio,kilometraje,descripcion,placa,imagen,nombreProp,cedulaProp,telefonoProp,direccionProp,emailProp");
            bw.newLine();
        }

        bw.write(marca + "," + modelo + "," + a침o + "," + precio + "," + kilometraje + "," + descripcion + "," +
                 placa + "," + rutaImagenSeleccionada + "," + nombreProp + "," + cedulaProp + "," + telefonoProp + "," +
                 direccionProp + "," + emailProp);
        bw.newLine();

        bw.close();
        fw.close();

        JOptionPane.showMessageDialog(this, "Veh칤culo guardado correctamente.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }
        

    }//GEN-LAST:event_btnguardarvehiculoActionPerformed

