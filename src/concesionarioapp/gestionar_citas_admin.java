    private void btncancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncancelarActionPerformed
        // TODO add your handling code here:
        int fila = tablacitas.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione una cita para eliminar.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "¬øEst√° seguro de eliminar esta cita?", "Confirmar", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        DefaultTableModel modelo = (DefaultTableModel) tablacitas.getModel();
        modelo.removeRow(fila);

        // Reescribe el archivo CSV
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("baseDeDatos/citas.csv"))) {
            for (int i = 0; i < modelo.getRowCount(); i++) {
                bw.write(modelo.getValueAt(i, 0) + "," +
                         modelo.getValueAt(i, 1) + "," +
                         modelo.getValueAt(i, 2) + "," +
                         modelo.getValueAt(i, 3) + "," +
                         modelo.getValueAt(i, 4));
                bw.newLine();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar cita: " + e.getMessage());
        }
    }
        
    }//GEN-LAST:event_btncancelarActionPerformed

